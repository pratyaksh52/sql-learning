USE LEARNING;

DROP TABLE IF EXISTS EMP;
DROP TABLE IF EXISTS DEPT;

-- CREATING THE EMP TABLE
CREATE TABLE EMP
(
    EMP_NO INT PRIMARY KEY NOT NULL,
    EMP_NAME VARCHAR(20),
    JOB VARCHAR(10),
    MGR_ID INT,
    HIRE_DATE DATE,
    SALARY DOUBLE(7, 2),
    COMMISSION DOUBLE(7, 2),
    DEPT_NO INT
);


-- CREATING THE DEPT TABLE
CREATE TABLE DEPT
(
    DEPT_NO INT PRIMARY KEY NOT NULL,
    DEPT_NAME VARCHAR(14),
    LOC VARCHAR(20)
);


